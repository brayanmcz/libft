#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;

	i = 0;
	while (*s1 != '\0')
	{
		s1++;
		i++;
	}
	while (*s2 != '\0' && n-- > 0)
	{
		*s1++ = *s2++;
		i++;
	}
	*s1 = '\0';
	while (i-- > 0)
		s1--;
	return (s1);
}
